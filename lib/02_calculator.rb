def add (x, y)

   return (x+y)
end

def subtract(z, a)
      
    return (z-a)
end

def sum(p)

   return (p).sum
end

def multiply (i, p)

   return (i*p)

end

def power (m, t)
   
   return (m**t)
end

def  factorial(j)

   if j ==0
      factorial(j) == 1
   else 
      (j*(j-1)*(j-2)*..*2*1) 
   end
   
end
